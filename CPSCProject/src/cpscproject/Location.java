package cpscproject;

public class Location {
	
	// Could alternatively change  x  and y to row and column if that's easier to understand. 
	// With this format, the x coordinate is the column and the  y coordinate is the row.
	 
	private int xcoord;
	private int ycoord;
	
	public int getX() {
		return xcoord;
	}
	
	public int getY() {
		return ycoord;
	}

}
